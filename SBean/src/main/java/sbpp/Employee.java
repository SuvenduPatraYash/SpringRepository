package sbpp;

public class Employee {

	// Class data members
	private String name;
	private String mail;

	// Getter
	public String getName() {
		return name;
	}

	// Setter
	public void setName(String name) {
		this.name = name;
	}

	// Getter
	public String getMail() {
		return mail;
	}

	// Setter
	public void setMail(String mail) {
		this.mail = mail;
	}
}